let x = 1;

// Relational Operators
console.log(x > 0);
console.log(x >= 1);
console.log(x < 1);
console.log(x <= 1);

// Equality Operators
console.log(x === 1);
console.log(x !== 1);

// Strict Equality (Same Type + Value)
console.log(x === 1);
console.log('1' === 1);

// Lose Equality
console.log(1 == 1);
console.log('1' == 1);